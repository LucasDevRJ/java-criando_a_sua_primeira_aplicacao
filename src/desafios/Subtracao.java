/**
 * 5. Crie uma classe Subtracao para imprimir o resultado de 10 - 5 no console.
 */

package desafios;

public class Subtracao {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        int subtracao = a - b;

        System.out.printf("%d - %d = %d", a, b, subtracao);
    }
}
